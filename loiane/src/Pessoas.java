public class Pessoas {
    private String nomes;
    private int idades;

    public Pessoas(String nomes, int idades) {
        this.nomes = nomes;
        this.idades = idades;
    }

    public String getNomes() {
        return nomes;
    }

    public void setNomes(String nomes) {
        this.nomes = nomes;
    }

    public int getIdades() {
        return idades;
    }

    public void setIdades(int idades) {
        this.idades = idades;
    }
}


